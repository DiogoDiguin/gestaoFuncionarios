package Model;

public class Regiao {
	public int idRegiao;
	private String nomeRegiao;
	
	public int getId() {
		return idRegiao;
	}
	public void setId(int idRegiao) {
		this.idRegiao = idRegiao;
	}
	public String getNomeRegiao() {
		return nomeRegiao;
	}
	public void setNomeRegiao(String nomeRegiao) {
		this.nomeRegiao = nomeRegiao;
	}
	
}
